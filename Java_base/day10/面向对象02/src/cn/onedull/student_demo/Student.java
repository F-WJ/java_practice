package cn.onedull.student_demo;

public class Student {
	private String name;
	private int age;
	
	//无参构造器
	public Student(){
		
	}
	
	//name
	public String getName(){
		return name;
	}
	public void setName(String name){
		this.name = name;
	}
	
	//age
	public int getAge(){
		return age;
	}
	public void setAge(int age){
		this.age = age;
	}
	
	
}
